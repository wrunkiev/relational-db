CREATE TABLE ORDERS(
ORDER_ID NUMBER,
CUSTOMER_ID NUMBER,
EMPLOYEE_ID NUMBER,
ORDER_DATE TIMESTAMP,
REQUIRED_DATE TIMESTAMP,
SHIPPED_DATE TIMESTAMP,
SHIP_VIA NUMBER,
FREIGHT NVARCHAR2(50),
SHIP_NAME NVARCHAR2(50),
SHIP_ADDRESS NVARCHAR2(50),
SHIP_CITY NVARCHAR2(50),
SHIP_REGION NVARCHAR2(50),
SHIP_POSTAL_CODE NVARCHAR2(50),
SHIP_COUNTRY NVARCHAR2(50),
CONSTRAINT ORDER_ID_PK PRIMARY KEY(ORDER_ID),
CONSTRAINT CUSTOMER_ID_FK FOREIGN KEY(CUSTOMER_ID) REFERENCES CUSTOMERS(CUSTOMER_ID),
CONSTRAINT EMPLOYEE_ID_FK FOREIGN KEY(EMPLOYEE_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID));